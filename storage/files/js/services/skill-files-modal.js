import { buildFileListEl, buildEditorEl } from './skill-files-editor.js';

export const buildModalEl = (skillName, files, selectedFile) => {
  const root = document.createElement('div');
  root.className = 'skill-modal-root';
  const header = document.createElement('div');
  header.className = 'skill-modal-header';
  const h2 = document.createElement('h2');
  h2.className = 'skill-modal-title';
  h2.textContent = skillName + ' - Files';
  header.append(h2);
  const btnGroup = document.createElement('div');
  btnGroup.className = 'skill-modal-btn-group';
  const syncBtn = document.createElement('button');
  syncBtn.className = 'btn btn-secondary btn-sm skill-modal-btn';
  syncBtn.id = 'skill-files-sync';
  syncBtn.textContent = 'Sync Files';
  const closeBtn = document.createElement('button');
  closeBtn.className = 'btn btn-secondary btn-sm skill-modal-btn';
  closeBtn.id = 'skill-files-close';
  closeBtn.textContent = 'Close';
  btnGroup.append(syncBtn);
  btnGroup.append(closeBtn);
  header.append(btnGroup);
  root.append(header);
  const body = document.createElement('div');
  body.className = 'skill-modal-body';
  const list = document.createElement('div');
  list.id = 'skill-files-list';
  list.className = 'skill-modal-sidebar';
  list.append(buildFileListEl(files, selectedFile));
  const editor = document.createElement('div');
  editor.id = 'skill-files-editor';
  editor.className = 'skill-modal-editor';
  editor.append(buildEditorEl(selectedFile));
  body.append(list);
  body.append(editor);
  root.append(body);
  return root;
};
